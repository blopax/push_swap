#include "libft.h"

int	ft_int_size(int nb)
{
	int	i;

	if (nb == -2147483648)
		return (11);
	if (nb < 0)
		nb = -nb;
	i = 1;
	while (nb >= 10)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
